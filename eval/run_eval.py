#!/usr/bin/env python3
"""
Legacy evaluator — headless CLI (button/CI only; not on the daily schedule).

The legacy framework: 20 automated regex checks (40%) + 7 quality dimensions judged
in a SINGLE OpenRouter LLM call (60%). Headless equivalent of the Gradio app's old
run_eval(), but it ALSO writes to state.json -> eval_history so the GitHub Pages
"Legacy Eval" tab refreshes. Kept purely for side-by-side comparison with AutoRubric.

Runs only when explicitly triggered (the Space/Pages "Run Legacy Eval" button →
run_legacy_eval.yml), never automatically — so it can't silently accumulate runs.
Same-day re-runs REPLACE the prior legacy entry rather than appending a duplicate.

Usage:  python -m eval.run_eval        (requires OPENROUTER_API_KEY)
Writes outputs/eval_<date>.md, updates state.json, prints SUMMARY_JSON=...
"""

from __future__ import annotations

import json
import os
import re
import sys
from datetime import datetime
from pathlib import Path

from dotenv import load_dotenv
from openai import OpenAI

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))
load_dotenv(ROOT / ".env")

from eval.evaluator import AutomatedEvaluator                       # noqa: E402
from eval.rubric import AUTOMATED_CHECKS, LLM_DIMENSIONS, AUTO_WEIGHT, LLM_WEIGHT  # noqa: E402

OUTPUTS = ROOT / "outputs"
STATE_PATH = OUTPUTS / "state.json"
OR_KEY = os.environ.get("OPENROUTER_API_KEY", "")
MODEL = os.environ.get("OPENROUTER_MODEL", "anthropic/claude-sonnet-4-6")
N_CHECKS = len(AUTOMATED_CHECKS)
DIM_IDS = [d["id"] for d in LLM_DIMENSIONS]


def _latest(pattern: str) -> Path | None:
    # Sort by filename (date-encoded), stable in CI where mtimes are equal after checkout.
    matches = sorted(OUTPUTS.glob(pattern), key=lambda p: p.name, reverse=True)
    return matches[0] if matches else None


def _run_checks(text: str | None) -> dict:
    if not text:
        return {}
    return {c.id: bool(c.passed) for c in AutomatedEvaluator().run(text)}


def _llm_pct(dims: dict) -> float:
    return sum(dims.get(d["id"], 0) * d["weight"] for d in LLM_DIMENSIONS) / 5


def _final(checks: dict, dims: dict) -> float:
    auto = (sum(checks.values()) / N_CHECKS) if checks else 0.0
    return (auto * AUTO_WEIGHT + _llm_pct(dims) * LLM_WEIGHT) * 100


def _excerpt(text: str | None, n: int = 3500) -> str:
    if not text:
        return "(not available)"
    return text if len(text) <= n else text[:2500] + "\n\n[…truncated…]\n\n" + text[-1000:]


def _build_prompt(v2: str, v1: str, gen: str) -> str:
    dims_desc = "\n".join(
        f"{i+1}. {d['name']} ({int(d['weight']*100)}%): {d.get('description','')[:200]}"
        for i, d in enumerate(LLM_DIMENSIONS)
    )
    keys = ",".join(f'"{k}":N' for k in DIM_IDS)
    return f"""You are evaluating three macro intelligence briefs for Emirates Development Bank.
Score each brief 1–5 on each dimension (1=worst, 5=best).

IMPORTANT NOTE on Trend & Continuity: v1 and General briefs have no cross-session state.json access.
They are structurally capped at 2/5 for Trend & Continuity regardless of prose quality.

DIMENSIONS:
{dims_desc}

--- BRIEF 1: v2 Agent (cross-session memory, streak language, CLAUDE.md feedback loop) ---
{_excerpt(v2)}

--- BRIEF 2: v1 Agent (EDB-structured pipeline, no cross-session memory) ---
{_excerpt(v1)}

--- BRIEF 3: General Agent (baseline Claude, no EDB tools or format) ---
{_excerpt(gen)}

Respond with ONLY valid JSON:
{{
  "v2":      {{{keys}}},
  "v1":      {{{keys}}},
  "general": {{{keys}}},
  "reasoning": {{"v2":"2-3 sentences","v1":"2-3 sentences","general":"2-3 sentences"}}
}}"""


def _fmt(v: bool) -> str:
    return "✓" if v else "✗"


def _report(date_str, names, checks, dims, finals) -> str:
    rows = "".join(
        f"| {c['name']:<50} | {_fmt(checks['v2'].get(c['id'], False))} "
        f"| {_fmt(checks['v1'].get(c['id'], False))} | {_fmt(checks['general'].get(c['id'], False))} |\n"
        for c in AUTOMATED_CHECKS
    )
    dim_rows = "".join(
        f"| {d['name']:<28} | {int(d['weight']*100)}% "
        f"| {dims['v2'].get(d['id'], '—')}/5 | {dims['v1'].get(d['id'], '—')}/5 "
        f"| {dims['general'].get(d['id'], '—')}/5 |\n"
        for d in LLM_DIMENSIONS
    )
    reasoning = dims.get("reasoning", {})
    auto = {t: sum(checks[t].values()) for t in ("v2", "v1", "general")}
    return f"""# EDB Agent Evaluation Report
**Date:** {date_str}
**v2 brief:** outputs/{names['v2']}
**v1 brief:** outputs/{names['v1'] or '(none)'}
**General brief:** outputs/{names['general'] or '(none)'}
**Rubric version:** legacy ({N_CHECKS} automated checks, 7 LLM dimensions, one-call judging)
**Generated by:** eval/run_eval.py (headless CI)

---

## Automated Checks (40% of score)

| Check | v2 | v1 | General |
|-------|:--:|:--:|:-------:|
{rows}| **TOTAL** | **{auto['v2']}/{N_CHECKS}** | **{auto['v1']}/{N_CHECKS}** | **{auto['general']}/{N_CHECKS}** |

---

## LLM Dimensions (60% of score)

| Dimension | Wt | v2 | v1 | General |
|-----------|:--:|:--:|:--:|:-------:|
{dim_rows}
### Reasoning

**v2 Agent:** {reasoning.get('v2', '—')}

**v1 Agent:** {reasoning.get('v1', '—')}

**General Agent:** {reasoning.get('general', '—')}

---

## Final Scores

|                      | v2 Agent | v1 Agent | General |
|----------------------|:--------:|:--------:|:-------:|
| Automated (40%)      | {auto['v2']/N_CHECKS*40:.1f} | {auto['v1']/N_CHECKS*40:.1f} | {auto['general']/N_CHECKS*40:.1f} |
| LLM Dimensions (60%) | {_llm_pct(dims['v2'])*60:.1f} | {_llm_pct(dims['v1'])*60:.1f} | {_llm_pct(dims['general'])*60:.1f} |
| **Final Score**      | **{finals['v2']:.1f} / 100** | **{finals['v1']:.1f} / 100** | **{finals['general']:.1f} / 100** |
| **v2 vs v1**         | **{finals['v2']-finals['v1']:+.1f} pts** | | |
| **v2 vs General**    | **{finals['v2']-finals['general']:+.1f} pts** | | |
"""


def _update_state(record: dict) -> None:
    if not STATE_PATH.exists():
        return
    try:
        state = json.loads(STATE_PATH.read_text())
    except Exception:
        return
    hist = state.setdefault("eval_history", [])
    # Replace any prior legacy entry for the same date (no duplicate accumulation).
    hist[:] = [e for e in hist
               if not (e.get("eval_date") == record["eval_date"] and e.get("framework") == "legacy")]
    hist.append(record)
    STATE_PATH.write_text(json.dumps(state, indent=2, default=str))


def main() -> None:
    if not OR_KEY:
        sys.exit("ERROR: OPENROUTER_API_KEY is not set — needed for LLM judging.")

    paths = {"v2": _latest("brief_v2_*.md"), "v1": _latest("brief_v1_*.md"), "general": _latest("brief_general_*.md")}
    if not paths["v2"]:
        sys.exit("ERROR: No v2 brief found in outputs/ — generate one first.")
    names = {t: (p.name if p else None) for t, p in paths.items()}
    texts = {t: (p.read_text(encoding="utf-8") if p else None) for t, p in paths.items()}
    print(f"Legacy eval | v2={names['v2']} v1={names['v1']} general={names['general']}", flush=True)

    checks = {t: _run_checks(texts[t]) for t in ("v2", "v1", "general")}
    print(f"Automated checks — v2: {sum(checks['v2'].values())}/{N_CHECKS}", flush=True)

    client = OpenAI(base_url="https://openrouter.ai/api/v1", api_key=OR_KEY,
                    default_headers={"X-Title": "EDB Legacy Eval"})
    print(f"Judging 7 dimensions × 3 briefs in one call ({MODEL})…", flush=True)
    resp = client.chat.completions.create(
        model=MODEL,
        messages=[{"role": "user", "content": _build_prompt(texts["v2"], texts["v1"] or "", texts["general"] or "")}],
        max_tokens=1500, temperature=0.1,
    )
    raw = re.sub(r"\s*```$", "", re.sub(r"^```(?:json)?\s*", "", resp.choices[0].message.content.strip()))
    llm = json.loads(raw)
    dims = {t: llm.get(t, {}) for t in ("v2", "v1", "general")}
    dims["reasoning"] = llm.get("reasoning", {})
    finals = {t: _final(checks[t], dims[t]) for t in ("v2", "v1", "general")}

    date_str = datetime.now().strftime("%Y-%m-%d")
    report_name = f"eval_{date_str}.md"
    (OUTPUTS / report_name).write_text(_report(date_str, names, checks, dims, finals))

    record = {
        "eval_date": date_str, "framework": "legacy", "report_path": f"outputs/{report_name}",
        "v2_brief_path": f"outputs/{names['v2']}",
        "v1_brief_path": f"outputs/{names['v1']}" if names["v1"] else None,
        "general_brief_path": f"outputs/{names['general']}" if names["general"] else None,
        "scores": {
            t: {"auto_passed": sum(checks[t].values()), "auto_total": N_CHECKS,
                "final_score": round(finals[t], 1),
                "dimension_scores": {k: dims[t].get(k) for k in DIM_IDS}}
            for t in ("v2", "v1", "general")
        },
        "deltas": {
            "v2_vs_v1": round(finals["v2"] - finals["v1"], 1),
            "v2_vs_gen": round(finals["v2"] - finals["general"], 1),
            "v1_vs_gen": round(finals["v1"] - finals["general"], 1),
        },
    }
    _update_state(record)

    print(f"\nReport written: outputs/{report_name}", flush=True)
    for t in ("v2", "v1", "general"):
        print(f"  {t:<8}: {finals[t]:.1f}/100  ({sum(checks[t].values())}/{N_CHECKS} checks)", flush=True)
    print("SUMMARY_JSON=" + json.dumps({
        "report_name": report_name, "date": date_str,
        "scores": {t: round(finals[t], 1) for t in ("v2", "v1", "general")},
    }), flush=True)


if __name__ == "__main__":
    main()
